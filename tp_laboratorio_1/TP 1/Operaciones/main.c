#include <stdio.h>
#include <stdlib.h>

int sumar(int a, int b)
{
    int suma;
    suma = a + b;
    return suma;
}


int restar(int a,int b)
{
    int resta;
    resta = a - b;
    return resta;
}


int multiplicar(int a, int b)
{
    int producto;
    producto = a * b;
    return producto;
}


int dividir(int a, int b)
{
    float cociente;
    cociente = a / b;
    return cociente;
}
